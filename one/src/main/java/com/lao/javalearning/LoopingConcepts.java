package com.lao.javalearning;

import java.util.Iterator;

public class LoopingConcepts {
	
	

	public static void main(String[] args) {
		
		  for(int i=0;i<26;i++) {
		  
		  System.out.println("25 times");
		  
		  
		  }
		 
		
		  int i=0; while(i<26) {
			  
		  
		  System.out.println("25 times"); i++;
		  
		  
		  }
		  
		
		int w=10;
		do {
			
			System.out.println("25 times"); 
			w++;
			
		} while (w<6);
		  
		

	}

}
